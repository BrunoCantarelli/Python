n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))

print('A soma entre os números é: {}'.format(n1 + n2))
print('A subtração entre os números é: {}'.format(n1 - n2))
print('A divisão entre os números é: {:.3f}'.format(n1 / n2))
print('A multiplicação entre os números é: {}'.format(n1 * n2))
print('A potência do primeiro número para o segundo é: {}'.format(n1 ** n2))
